// Array Methods.

// concat : merge 2 arrays.

let primary = ["red","yellow","blue"];
let secondary = ["orange","green","violet"];

let allColors = primary.concat(secondary);
console.log(allColors);

// reverse : reverse an array
console.log(allColors.reverse());

