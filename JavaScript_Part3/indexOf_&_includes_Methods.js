// Array Methods

// indexOf : returns index of something

let col = ['red','yellow','green','blue'];
console.log(col.indexOf('blue'));

let cars = ['BMW','Audi','Xuv','Doze'];
console.log(cars.indexOf('BMW'));

// includes : search for a value

let marks = [35,50,65,75,85,95,100]
console.log(marks.includes(40)); //false
console.log(marks.includes(75)); // true
